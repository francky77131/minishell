#include "minishell.h"

int	quote_check(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if (str[i] == '"')
			while (str[++i] != '"')
				if (!str[i])
					return (1);
		if (str[i] == '\'')
			while (str[++i] != '\'')
				if (!str[i])
					return (1);
	}
	return (0);
}
