#include "../minishell.h"

int	isasep(const char c)
{
	if (c == 39)
		return (1);
	if (c == 34)
		return (1);
	if (c == '<')
		return (1);
	if (c == '>')
		return (1);
	if (c == '|')
		return (1);
	return (0);
}
