#include "../minishell.h"

size_t	ft_str_len(char const *s)
{
	int	i;

	i = 0;
	if (s[i] == 39)
	{
		i++;
		while (s[i] && s[i] != 39)
			i++;
		i++;
	}
	else if (s[i] == 34)
	{
		i++;
		while (s[i] && s[i] != 34)
			i++;
		i++;
	}
	else if (isasep(s[i]))
	{
		i++;
		if (s[i] && s[i - 1] && s[i] == '<' && s[i - 1] == '<')
			i++;
		if (s[i] && s[i - 1] && s[i] == '>' && s[i - 1] == '>')
			i++;
	}
	else
		while (s[i] && s[i] != ' ' && !isasep(s[i]))
			i++;
	return (i);
}
