#include "../minishell.h"

size_t	ft_lstsize(t_pile *pile)
{
	t_pile	*v;
	size_t	i;

	if (!pile)
		return (0);
	i = 0;
	v = pile;
	while (v)
	{
		v = v->next;
		i++;
	}
	return (i);
}
